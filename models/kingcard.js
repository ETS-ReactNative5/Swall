class KingCard {
    constructor(
        id,
        gameId,
        title,
        subTitle,
        description,
        imageIcon,
        number,
        color,
    ) {
        this.id = id;
        this.gameId = gameId,
        this.title = title;
        this.subTitle = subTitle;
        this.description = description;
        this.imageIcon = imageIcon;
        this.number = number;
        this.color = color;
    }
}
export default KingCard;